package com.google.android.gnd.model;

import androidx.annotation.NonNull;
import com.google.auto.value.AutoValue;
import java.util.Date;
import java8.util.Optional;

/** User details and timestamp for creation or modification of a model object. */
@AutoValue
public abstract class AuditInfo {

  /**
   * Returns the user initiating the related action. This can never be null, since users must always
   * be logged in to make changes.
   */
  @NonNull
  public abstract User getUser();

  /** Returns the time at which the user action was initiated, according to the user's device. */
  @NonNull
  public abstract Date getClientTimeMillis();

  /**
   * Returns the time at which the server received the requested change according to the server's
   * internal clock, or empty if the updated server time was not yet received.
   */
  @NonNull
  public abstract Optional<Date> getServerTimeMillis();

  /** Returns a new instance initialized with the current client time and the specified user. */
  public static AuditInfo now(User user) {
    return AuditInfo.builder().setUser(user).setClientTimeMillis(new Date()).build();
  }

  // Generated by AutoValue plugin:

  public static Builder builder() {
    return new AutoValue_AuditInfo.Builder().setServerTimeMillis(Optional.empty());
  }

  @AutoValue.Builder
  public abstract static class Builder {

    public abstract Builder setUser(User newUser);

    public abstract Builder setClientTimeMillis(@NonNull Date newClientTimeMillis);

    public abstract Builder setServerTimeMillis(@NonNull Optional<Date> newServerTimeMillis);

    public abstract AuditInfo build();
  }
}
